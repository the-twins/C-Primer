// The program uses one printf() call to print your name and last name on one
// line, uses second printf() call to print your first and last names on two
// separate lines, and uses a pair of printf() calls to print your first and
// last names on one line.
#include <stdio.h>

int main(void)
{
    printf("Novosad Marina\n");
    printf("Novosad\nMarina\n");
    printf("Novosad ");
    printf("Marina\n");

    return 0;
}
